int	ft_sqrt(int nb)
{
	long	i;
	long	square;

	if (nb <= 0)
		return (0);
	i = 1;
	while (i * i <= nb)
	{
		square = i * i;
		if (square == nb)
			return (i);
		i++;
	}
	return (0);
}
